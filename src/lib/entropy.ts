export function generateCoordinate(): string {
    const part1 = Math.floor(Math.random() * 9000) + 1000;
    const part2 = Math.floor(Math.random() * 9000) + 1000;
    return `${part1}-${part2}`;
}

export function generatePromptFromHash(hash: string): string {
    // We use distinct segments of the hash to ensure independence between 
    // Subject, Context, and Style.
    // Parsing 4 hex characters gives a range of 0-65535, offering uniform distribution.

    const subHash = parseInt(hash.substring(0, 4), 16) || 0;
    const ctxHash = parseInt(hash.substring(4, 8), 16) || 0;
    const styHash = parseInt(hash.substring(8, 12), 16) || 0;

    const subject = SUBJECTS[subHash % SUBJECTS.length];
    const context = CONTEXTS[ctxHash % CONTEXTS.length];
    const style = STYLES[styHash % STYLES.length];

    return `${subject} ${context}, ${style} style.`;
}
